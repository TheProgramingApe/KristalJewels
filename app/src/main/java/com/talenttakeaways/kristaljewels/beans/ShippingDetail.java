public class ShippingDetail {

    private String userId,
            shippingId,
            shoppeName,
            shoppeAddress,
            shoppeLocation,
            shoppeState,
            shoppeCountry,
            shoppePhoneNumber;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getShippingId() {
        return shippingId;
    }

    public void setShippingId(String shippingId) {
        this.shippingId = shippingId;
    }

    public String getShoppeName() {
        return shoppeName;
    }

    public void setShoppeName(String shoppeName) {
        this.shoppeName = shoppeName;
    }

    public String getShoppeAddress() {
        return shoppeAddress;
    }

    public void setShoppeAddress(String shoppeAddress) {
        this.shoppeAddress = shoppeAddress;
    }

    public String getShoppeLocation() {
        return shoppeLocation;
    }

    public void setShoppeLocation(String shoppeLocation) {
        this.shoppeLocation = shoppeLocation;
    }

    public String getShoppeState() {
        return shoppeState;
    }

    public void setShoppeState(String shoppeState) {
        this.shoppeState = shoppeState;
    }

    public String getShoppeCountry() {
        return shoppeCountry;
    }

    public void setShoppeCountry(String shoppeCountry) {
        this.shoppeCountry = shoppeCountry;
    }

    public String getShoppePhoneNumber() {
        return shoppePhoneNumber;
    }

    public void setShoppePhoneNumber(String shoppePhoneNumber) {
        this.shoppePhoneNumber = shoppePhoneNumber;
    }
}